using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace QLST.DT0
{
    class Khohang
    {
        private string makho;
        private string tenkho;
        private string mathukho;
        private string diachi;

        public string Makho
        {
            get
            {
                return makho;
            }

            set
            {
                makho = value;
            }
        }

        public string Tenkho
        {
            get
            {
                return tenkho;
            }

            set
            {
                tenkho = value;
            }
        }

        public string Mathukho
        {
            get
            {
                return mathukho;
            }

            set
            {
                mathukho = value;
            }
        }

        public string Diachi
        {
            get
            {
                return diachi;
            }

            set
            {
                diachi = value;
            }
        }
    }
}
